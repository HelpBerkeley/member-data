package org.helpberkeley.memberdata.route;

public class Location {
    private final String address;
    private final double latitude;
    private final double longitude;

    Location(String address, Location location) {
        this.address = address;
        this.latitude = location.latitude;
        this.longitude = location.longitude;
    }

    Location(String address, double latitude, double longitude) {
        this.address = address;
        this.latitude = latitude;
        this.longitude = longitude;
    }

    public String getAddress() {
        return address;
    }

    public double getLatitude() {
        return latitude;
    }

    public double getLongitude() {
        return longitude;
    }

    @Override
    public int hashCode() {
        Double latLng = latitude * longitude;
        return latLng.hashCode();
    }

    @Override
    public boolean equals(Object obj) {

        return (obj instanceof Location)
                && (((Location)obj).latitude == latitude)
                && (((Location)obj).longitude == longitude);
    }
}
